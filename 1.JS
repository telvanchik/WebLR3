console.log("" + 1 + 0) // Так как первый операнд является строкой, значит оставшиеся операнды также будут преобразованы в строку
console.log("" - 1 + 0) // В данном случае есть оператор вычитания, значит все операнды будут преобразованы с числа
console.log(true + false) // Преобразование True - 1, False - 0 
console.log(6 / "3") // Деление, оба операнда являются числами
console.log("2" * "3") //  Умножение, оба операнда числа
console.log(4 + 5 + "px") // Сложение, есть операнд строкового типа
console.log("&" + 4 + 5) // Сложение, есть операнд строкового типа
console.log("4" - 2) // Вычитание, оба операнда будут преобразованы в числа
console.log("4px" - 2) // Ошибка, первый операнд не число
console.log(" -9 " + 5) // Так как первый операнд строка и сложение
console.log(" -9 " - 5) // Вычитание, операнды будут преобразованы в числа
console.log(null + 1) // Преобразование null - 0
console.log(undefined + 1) // Преобразование undefined - nan, любая операция с nan возвращает nan
console.log(" \t \n" - 2) // Знак табуляции и новой строки


console.log(5 > 4) // Результат сравение true
console.log("ананас" > "яблоко") // Проверка в алфавитном порядке
console.log("2" > "12") // По порядку 2 следует за 1, значит true, так как сравнение строк
console.log(undefined == null) // Особенность джавы
console.log(undefined === null) // Различные типы
console.log(null == "\n0\n") // Для равенства null не преобразуется
console.log(null === +"\n0\n") // Для равенства null не преобразуется